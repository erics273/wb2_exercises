//create variables for our coordinates
let x1 = 1;
let y1 = 2;
let x2 = 4;
let y2 = 6;

//calculate the distance between the coordinates using the formula
let distance = Math.sqrt(Math.pow(x2 - x1, 2) + Math.pow(y2 - y1, 2));

//display the distance between the coordinates in the console
console.log("Distance between coordinates: ", distance);